    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jffValorUnitarioInicial.getDocument().getLength() > 5 || jffValorUnitarioFinal.getDocument().getLength()> 5) {
            JOptionPane.showMessageDialog(null, "Não são aceitos valores na casa da centena ou acima", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("/relatorios/relatorio_produto.jrxml"));

            Map parametros = new HashMap();
            parametros.put("valorUnitarioInicial", jffValorUnitarioInicial.getText());
            parametros.put("valorUnitarioFinal", jffValorUnitarioFinal.getText());

            JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ConexaoBD.getInstance().getConnection());
            JasperViewer.viewReport(impressao, false);

            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gerar relatório: " + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

